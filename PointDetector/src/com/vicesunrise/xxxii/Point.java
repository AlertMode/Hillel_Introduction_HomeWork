package com.vicesunrise.xxxii;

public class Point {
    private int abscissa;
    private int ordinate;

    public Point(int abscissa, int ordinate){
        this.abscissa = abscissa;
        this.ordinate = ordinate;
    }

    public int getAbscissa() {
        return abscissa;
    }

    public int getOrdinate() {
        return ordinate;
    }
}